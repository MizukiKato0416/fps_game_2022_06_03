CNumber::CNumber(CObject::PRIORITY Priority) : CObject2D(Priority)
{
	
}

//=============================================================================
// デフォルトデストラクタ
//=============================================================================
CNumber::~CNumber()
{

}

//=============================================================================
// ポリゴンの初期化処理
//=============================================================================
HRESULT CNumber::Init(void)
{
	CObject2D::Init();
	CObject2D::SetPos(m_Pos, m_Size);
	CObject2D::BindTexture(CManager::GetInstance()->GetTexture()->GetTexture(m_Type));

	return S_OK;
}

//=============================================================================
// ポリゴンの終了処理
//=============================================================================
void CNumber::Uninit(void)
{
	CObject2D::Uninit();
}

//=============================================================================
// ポリゴンの更新処理
//=============================================================================
void CNumber::Update(void)
{

}

//=============================================================================
// ポリゴンの描画処理
//=============================================================================
void CNumber::Draw(void)
{
	CObject2D::Draw();
}

//=============================================================================
// ゲッダー
//=============================================================================
CNumber *CNumber::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size, string Type)
{
	CNumber *pNumber = NULL;
	if (pNumber == NULL)
	{
		pNumber = new CNumber;
	}

	pNumber->m_Pos = pos;
	pNumber->m_Size = size;
	pNumber->m_Type = Type;

	if (pNumber != NULL)
	{
		pNumber->Init();
	}
	return pNumber;
}